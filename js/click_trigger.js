// introduction menu box

function toggleMenuI() {
  var menuBoxI = document.getElementById('menu-box-i');
  if(menuBoxI.style.display == "grid") { // if is menuBox displayed, hide it
    menuBoxI.style.display = "none";
  }
  else { // if is menuBox hidden, display it
    menuBoxI.style.display = "grid";
  }

  var menuImageI = document.getElementById('menu-image-i');
  if(menuImageI.style.display == "block") { // if is menuBox displayed, hide it
    menuImageI.style.display = "none";
  }
  else { // if is menuBox hidden, display it
    menuImageI.style.display = "block";
  }
}

